# Task 5 - Find out and describe when will a server return 200, 401, 404, 429, 500, 418 status codes

# 200 - HTTP 200 OK success
# The HTTP 200 OK success status response code indicates that the request has succeeded. A 200 response is cacheable
# by default. The meaning of a success depends on the HTTP request method: GET : The resource has been fetched and is
# transmitted in the message body.

# 401 - The HyperText Transfer Protocol (HTTP) 401 Unauthorized response status code indicates that the client request
# has not been completed because it lacks valid authentication credentials for the requested resource - не си логнат,
# за да достъпиш това

# 404 - 404 is a status code that tells a web user that a requested page is not available. 404 and other response
# status codes are part of the web's Hypertext Transfer Protocol response codes. The 404 code means that a server
# could not find a client-requested webpage.

# 429 - 429 Too Many Requests
# The HTTP 429 Too Many Requests response status code indicates the user has sent too many requests in a given amount
# of time ("rate limiting"). A Retry-After header might be included to this response indicating how long to wait before
# making a new request

# 500 - The HTTP status code 500 is a generic error response. It means that the server encountered an unexpected
# condition that prevented it from fulfilling the request. This error is usually returned by the server when no
# other error code is suitable


# 418 - I'm a teapot
# The HTTP 418 I'm a teapot client error response code indicates that the server refuses to brew coffee because it is,
# permanently, a teapot. A combined coffee/tea pot that is temporarily out of coffee should instead return 503.
# Some websites use this response for requests they do not wish to handle, such as automated queries.



