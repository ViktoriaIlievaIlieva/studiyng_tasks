# ⦁	Операции между числа
# Напишете програма, която чете две цели числа (N1 и N2) и оператор, с който да се извърши дадена математическа
# операция. Възможните операции са: Събиране(+), Изваждане(-), Умножение(*),  Деление(/) и Модулно деление(%).
# При събиране, изваждане и умножение на конзолата трябва да се отпечатат резултата и дали той е четен или нечетен.
# При обикновеното деление - резултата. При модулното деление - остатъка. Трябва да се има предвид, че делителят може
# да е равен на 0 (нула), а на нула не се дели. В този случай трябва да се отпечата специално съобщениe.
# Вход
# От конзолата се прочитат 3 реда, въведени от потребителя:
# ⦁	N1 - цяло число;
# ⦁	N2 - цяло число;
# ⦁	Оператор - един символ измежду: "+", "-", "*", "/", "%".

# Изход
# Да се отпечата на конзолата един ред:
# ⦁	Ако операцията е събиране, изваждане или умножение:
# ⦁	 "{N1} {оператор} {N2} = {резултат} - {even/odd}"
# ⦁	Ако операцията е деление:
# ⦁	"{N1} / {N2} = {резултат}" - резултат, форматиран до втория знак след десетичната запетая
# ⦁	Ако операцията е модулно деление:
# ⦁	"{N1} % {N2} = {остатък}"
# ⦁	В случай на деление с 0 (нула):
# ⦁	"Cannot divide {N1} by zero"

num_1: int = int(input())
num_2: int = int(input())
operator: str = input()
result: float = 0
result_number: str = ""

if operator == "+" or operator == "-" or operator == "*":
    if operator == "+":
        result = num_1 + num_2
    elif operator == "-":
        result = num_1 - num_2
    elif operator == "*":
        result = num_1 * num_2

    if result % 2 == 0:
        result_number = "even"
    else:
        result_number = "odd"

    print(f"{num_1} {operator} {num_2} = {result} - {result_number}")

elif operator == "/":
    if num_2 != 0:
        result = num_1 / num_2
        print(f"{num_1} / {num_2} = {result:.2f}")
    else:
        print(f"Cannot divide {num_1} by zero")

elif operator == "%":
    if num_2 != 0:
        result = num_1 % num_2
        print(f"{num_1} % {num_2} = {result}")
    else:
        print(f"Cannot divide {num_1} by zero")

