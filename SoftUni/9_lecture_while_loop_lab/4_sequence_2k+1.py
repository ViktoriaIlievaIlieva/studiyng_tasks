# ⦁	Редица числа 2k + 1
# Напишете програма, която чете число n, въведено от потребителя, и отпечатва всички числа  ≤  n от редицата:
# 1, 3, 7, 15, 31, …. Всяко следващо число се изчислява като умножим предишното с 2 и добавим 1.

n: int = int(input())

index = 1
while index <= n:
    print(index)
    index = index * 2 + 1





