# Четно или нечетно
# Да се напише програма, която чете цяло число, въведено от потребителя, и печата дали е четно или нечетно.
# Ако е четно отпечатайте "even", ако е нечетно "odd".

x: int = int(input())
if x % 2 == 0:
    print("even")
else:
    print("odd")

# variant 2

print("even" if x % 2 == 0 else "odd")
